

--RELATORIO DE FATURAMENTO - INTERVALO DE DATAS POR DTNEG

SELECT DISTINCT
CAB.CODTIPOPER TOPS,
TOP.DESCROPER DESCRTOP,
(CASE
WHEN (UFS.UF = 'MG') THEN TOP.CODCFO_SAIDA
WHEN (UFS.UF <> 'MG') THEN TOP.CODCFO_SAIDA_FORA
END) AS "CFOP",
UFS.UF,
CAB.NUNOTA,
CAB.NUMNOTA,
ITE.SEQUENCIA,
CAB.DTFATUR,
CAB.DTENTSAI,
CAB.DTMOV DTMOV,
CAB.DTNEG,
CAB.CODEMP,
EMP.RAZAOSOCIAL "EMPRESA",
CAB.CODPARC,
PAR.CGC_CPF,
PAR.RAZAOSOCIAL "PARCEIRO",
CAB.TIPMOV,
ITE.CODPROD,
PRO.DESCRPROD,
GRU.DESCRGRUPOPROD,
PRO.USOPROD,
ITE.CODTRIB,
PRO.NCM,
PRO.CODVOL,
VEN.APELIDO,
CAB.VLRDESCTOTITEM,
(CASE
WHEN CAB.CODTIPOPER IN (1200,1201,1215,1216,1217) THEN ITE.QTDNEG * -1
WHEN CAB.CODTIPOPER IN (1100,1112,1152,1105,1153) THEN ITE.QTDNEG END) AS QTDNEG,
ITE.VLRUNIT,
(CASE WHEN (TTLVLR = 0) THEN 0 ELSE (
((CASE
WHEN CAB.CODTIPOPER IN (1200,1201,1215,1216,1217) THEN ITE.VLRTOT * -1
WHEN CAB.CODTIPOPER IN (1100,1112,1152,1105,1153) THEN ITE.VLRTOT END)/TTLVLR))END) AS PERC,

(CASE WHEN (TTLVLR = 0) THEN 0 ELSE (
(((CASE
WHEN CAB.CODTIPOPER IN (1200,1201,1215,1216,1217) THEN ITE.VLRTOT * -1
WHEN CAB.CODTIPOPER IN (1100,1112,1152,1105,1153) THEN ITE.VLRTOT END) /TTLVLR) * CAB.VLRNOTA))END) AS VLR_PERC_NOTA,
ITE.VLRREPRED,
CAB.VLRFRETE,
CAB.ISSRETIDO,
(CAB.VLRISS * 
(CASE WHEN (TTLVLR = 0) THEN 0 ELSE (
((CASE
WHEN CAB.CODTIPOPER IN (1200,1201,1215,1216,1217) THEN ITE.VLRTOT * -1
WHEN CAB.CODTIPOPER IN (1100,1112,1152,1105,1153) THEN ITE.VLRTOT END)/TTLVLR) )END)) AS VLRISS,

(CASE WHEN CAB.ISSRETIDO = 'S' THEN 
(CAB.VLRISS * 
(CASE WHEN (TTLVLR= 0) THEN 0 ELSE (
((CASE
WHEN CAB.CODTIPOPER IN (1200,1201,1215,1216,1217) THEN ITE.VLRTOT * -1
WHEN CAB.CODTIPOPER IN (1100,1112,1152,1105,1153) THEN ITE.VLRTOT END)/TTLVLR) )END))+
(CASE WHEN (TTLVLR = 0) THEN 0 ELSE (
(((CASE
WHEN CAB.CODTIPOPER IN (1200,1201,1215,1216,1217) THEN ITE.VLRTOT * -1
WHEN CAB.CODTIPOPER IN (1100,1112,1152,1105,1153) THEN ITE.VLRTOT END) /TTLVLR) * CAB.VLRNOTA) )END) 
ELSE 
(CASE WHEN (TTLVLR = 0) THEN 0 ELSE (
(((CASE
WHEN CAB.CODTIPOPER IN (1200,1201,1215,1216,1217) THEN ITE.VLRTOT * -1
WHEN CAB.CODTIPOPER IN (1100,1112,1152,1105,1153) THEN ITE.VLRTOT END) /TTLVLR) * CAB.VLRNOTA) )END) 
END) VLR_PROP_PROD_COM_ISS
FROM TGFCAB CAB
INNER JOIN TGFITE ITE ON CAB.NUNOTA = ITE.NUNOTA
INNER JOIN TSIEMP EMP ON CAB.CODEMP = EMP.CODEMP
INNER JOIN TGFPAR PAR ON CAB.CODPARC = PAR.CODPARC
INNER JOIN TGFTOP TOP ON CAB.CODTIPOPER = TOP.CODTIPOPER
INNER JOIN TGFPRO PRO ON ITE.CODPROD = PRO.CODPROD
INNER JOIN TGFGRU GRU ON PRO.CODGRUPOPROD = GRU.CODGRUPOPROD
INNER JOIN TSICID CID ON PAR.CODCID = CID.CODCID
INNER JOIN TSIUFS UFS ON CID.UF = UFS.CODUF
INNER JOIN (SELECT ITE.NUNOTA, SUM(VLRTOT) AS TTLVLR
FROM TGFITE ITE GROUP BY ITE.NUNOTA) ITE2 ON ITE.NUNOTA = ITE2.NUNOTA
INNER JOIN TGFVEN VEN ON CAB.CODVEND = VEN.CODVEND
WHERE 
(CAB.DTNEG >= TO_DATE('2023-06-06','YYYY-MM-DD') AND CAB.DTNEG <= TO_DATE('2023-06-20','YYYY-MM-DD'))
--(CAB.DTNEG >= :P_PERIODO.INI AND CAB.DTNEG <= :P_PERIODO.FIN)
AND CAB.CODEMP IN (60)
--AND (CAB.CODVEND IN :P_CODVEND)
--AND (PRO.CODGRUPOPROD IN :P_CODGRUPOPROD)
--AND (CAB.CODTIPOPER IN :P_TOP)
AND CAB.CODTIPOPER  IN (1100,1112,1152,1200,1201,1215,1216,1217,1105,1153)
AND CAB.STATUSNOTA = 'L'
ORDER BY 8,5,7




